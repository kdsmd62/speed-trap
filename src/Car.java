public class Car {
    private int distance;
    private int speed;

    public Car(int distance, int speed) {
        this.distance = distance;
        this.speed = speed;
    }

    public int getDistance() {
        return distance;
    }

    public void setDistance(int distance) {
        this.distance = distance;
    }

    public int getspeed() {
        return speed;
    }

    public void setspeed(int speed) {
        this.speed = speed;
    }
}
